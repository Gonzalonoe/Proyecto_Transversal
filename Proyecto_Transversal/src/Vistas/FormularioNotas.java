    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
       
        int filaSelecionada = jTmateriascursadas.getSelectedRow();
         
         if (filaSelecionada!=-1) {
             
            Alumno a = (Alumno) jClistaralumnos.getSelectedItem();
            
            int idMateria= (int) modelo.getValueAt(filaSelecionada, 0);
            double nota = (double) modelo.getValueAt(filaSelecionada, 2);
            
            inscData.actualizarNota(a.getIdAlumno(), idMateria, nota);
            
            borrarFilas();
            
        }
        
    }//GEN-LAST:event_jBGuardarActionPerformed

    private void jClistaralumnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jClistaralumnosActionPerformed
      
      borrarFilas();
      Alumno alu =  (Alumno) jClistaralumnos.getSelectedItem();
      
      listaIsnc = inscData.obtenerInscripcionesPorAlumno(alu.getIdAlumno());
      
      
      for( Inscripcion ins : listaIsnc){
            modelo.addRow(new Object[] {ins.getIdInscripcion(),ins.getIdMateria().getNombre(),ins.getNota()});
        }
      
     
    }//GEN-LAST:event_jClistaralumnosActionPerformed
    
    private void cargarAlumnos(){
        for(Alumno item:listaAlu){
            jClistaralumnos.addItem(item);
        }
    }
private void armarCabeceraTabla(){
        
    ArrayList<Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("ID de Materia");
        filaCabecera.add("Nombre");
        filaCabecera.add("Nota");
        for (Object it : filaCabecera){
            modelo.addColumn(it);
        }
        jTmateriascursadas.setModel(modelo);
    }
    
    private void borrarFilas(){
        int indice = modelo.getRowCount()-1;
        
        for (int i = indice; i >=0; i--) {
            modelo.removeRow(i);
        }
    }
